<p>Delivering Performance – front line team leaders and team members </p>

<p>An auto component manufacture making engines parts for the 2 and 4 wheeler - Cylinder Heads, crank case, engine housing , support bracket and other products.</p>  


<p><strong>Challenges </strong></p>

<p>Getting performances from the front line team members and team leaders have always been the challenging one. The operational excellence through higher value add and elimination of wastages are key challenges. Team is there , however we have to get the best out of team. There is machine, material and good processes, the challenges is how to achieve the target. How to improve the line performances and improve and overall plant efficiency. Front line team members involvement through kaizen and suggestion scheme has also been challenges.</p> 

<p><strong>TPP intervention and Approach </strong></p>

<p>Based on management briefing and understanding, the analysis was done for the front line team leaders roles and responsibility as well as their intervention were focused. The intervention was based on developing the accountability through 3Cs and 3 Is.</p> 

<p>3 Cs- Connect- communicate- commitment – all 3 factor were considered as key factor. Programmes were designed and develop as per need. How to understand and relate with team members was the key focus. This focused on interpersonal relations and team building. How to connect with the team members and get best from them. </p>

<p>The second level of intervention was on <strong>3Is</strong> – involve , improve –impact through various tools and technique. The focus was to go to the root and improve the performances. Focus on continual improvement at the shop floor through use of PDCA cycle and root cause analysis. Use of QC tools and technique. </p>

<p>The development process was measured through people potential analysis and assigned home work. Based on potential analysis , feedback wad given the improvement points were suggested to each team leader. There was regular review with their respect functional heads. At the shop floor the performance were monitored and required guidance and support were provided. Feedback to the management was given on periodic basis. </p>

<p><strong>Impact</strong></p> 

<p>Overall there has been growth in business. The operational efficiency improved in the respective areas. Team leaders also encouraged self and others to give ideas and suggestion. Focus on innovative approach. There were number of process related challenges, which were addressed and resolved.</p> 

