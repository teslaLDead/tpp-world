<p> Manpower Requirement Analysis Project </p>

<p>TPP association with Unipart group for a longer period specially for the manpower requirement analysis project. Completed 2<br> projects with 2 units of uniparts group. There has also been intervention for the development of middle level. </p>

<p><strong>Challenges </strong></p>
 
<p>As the organization is expanding its capacity and there is  merger and acquisition of 2 group companies, there is always the need to understand what would be manpower requirement. Traditionally based on existing capacity, product and processes, customer demand the manpower requirement in terms of number is decided, however this is not always the right method of deciding the required manpower. </p>

<p>Deciding the required manpower in terms of number as well quality of manpower based on competence is difficult to determine. Right people at the right place with right number is a challenging one to decide. </p>

<p><strong>TPP intervention and Approach </strong></p>

<p>Based on briefing received from the managment and HR team proposal was prepared to carry out the systematic study of manpower requirement in the organization. Process  started with collecting data and facts and visit to the plant to understand the existing product and process as well as products and processes to be added in future with added capacity. </p>

<p>The mandate was to come out the right number and quality of manpower in the front line as well as middle and senior level leadership role. The functional and section heads in respective departments. By following TPPP operating model all functions , processes and sub processes as well as existing leaders were mapped. The systematic study was done to understand the difficulty level of each process and sub processes. The value add at each level was also examined and discussed. </p>

<p>Based on detailed study, data and fact analysis was done and reports were prepared and discussed at multiple levels. Mainly 4 divisions created with focused approach and number of functions were realigned and merged with. The real challenge was new business development and operations. Report submitted and accepted. </p>

<p><strong>Impact</strong></p> 

<p>The final outcome of the project was the number and quality of manpower required for the existing as well as new business expansion. With the existing manpower, there were number of them able to carry out the new expansion work. Overall required manpower number was much lesser than what was earlier predicted by the internal managment team . The deployment process started as per the recommendations. </p>

