    <div id="d" class="tab-pane fade">
     <h3 class="secand-sec-bluebg"><strong>Demanding Performance ( Middle Level )</strong><span class="pull-right"><a href="pdf-folder/Demanding Performnces.pdf"><img src="images/pdf-icon-2.jpg" alt="" data-toggle="tooltip" title="Download pdf"/></a></span> </h3>
<p><strong>Challenges</strong><br> Building performance culture in the organization is the key challenges for the organization.  Aligning the business goals and targets with the individual  key performance index is the challenging one. There is need to align the business plan with departmental objective and individual KRAs ( Key Result Areas). It has to be set on time and measured the performance on periodic basis. This is very much required to achieve the desired business result. The rating points are also linked with the rewards and recognition , therefore there has to <strong>be SMART approach</strong> and objectivity. </p>
<p><strong>TPP Approach </strong><br>
TPP follows the TPPP operating models setting targets and goals. Based on business plan and departmental objective the individual key performance index / key performance areas are discussed and decided. SMART target are set and it is decided for a given period of time, preferably on quarterly basis.</p>

<p>TPP works through the workshop approach , where starting from vision and mission the current year plus 3 years the business plan is decided and annual operating plan ( AoP) is prepared. The department objective is also finalized and then the individual KRAs / targets are decided. The major focus is improvement and breakthrough. The level based targets are set and difficulty level is also decided.</p>

<p>TPP supports in periodic review and appraisal. Time to time as per business requirement, the key result areas as well as targets are redefined and targets are achieved. Periodic feedback and counseling is imparted. TPP team provides the feedback and support.</p>

<p><strong>Clients benefit</strong><br>
Over the period of 2-3 years , the organization has achieved the breakthrough result. There has been focused approach. More than 200 percent growth in sales as well in margin has been achieved through this process. There is trust and transparency in the organization  . Rewards and recognition is given as per the value add /contribution. Career progression and growth has also been decided based on performance system.</p>

