  <h3 class="secand-sec-bluebg"><strong>Manpower Deployment </strong></h3>
<p><strong>Challenges </strong><br>
Business Sustainability and scalability as well as retention, improvement and breakthrough in work are differentiated through roles and responsibility. It is very important to define the roles and responsibility of each functions based on different levels of employees in the organization. A clear cut understanding is very much required otherwise irrespective of all efforts, organization would not get benefits. The salary and  wages bill would go up however result would not be achieved.  Placing the right person with the right job and accountability is the key challenges. </p>

 <p><strong>TPP approach </strong><br>
TPP understand the given function in the organization along with  inter-department overlaps. Overall product and process flow as well as customer requirement. 
</p>

<p><strong>Process based role matrix</strong> is the mantra of success. Mapping all processes and sub process and preparing the role based on processes is the key thrust areas. </p>

<p>We map all process and difficulty level and based on which the roles and responsibility is defined among team members. It is ensured that none of the processes and sub processes are left out so that there is complete coverage of all areas.  How we can add efficiency and effectiveness to the position. </p>

<p>Along with process based role matrix, we also define the<strong> work plan</strong>.  How to manage, daily, weekly, monthly and yearly activity with report.  Reports to be generated is also mapped for. </p>

<p><strong>Client Benefits </strong><br>
Clients have benefited from the defined roles and responsibility. It has not only brought the role clarity , it has added to the productivity and performance. The role ambiguity has been eliminated and level specific roles and responsibility has been defined.  Value add , value paid has been emphasized. Functional and operational efficiency has been improved by focusing on value added and non value added activity. Work efficiency has improved. 
</p>

