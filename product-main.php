     <p><u>Challenges </u><br>
Organizations have grown over the period of time and engaged the required manpower as the business has grown. As the business expansion takes place or there is less demand of product, there is variation of manpower demand. It becomes very difficult for the management and HR department to determine how many manpower would be required as well as what would be the quality of manpower. How to deploy the required manpower with right talent. A difficult situation emerges , where it is difficult the decide how to proceed for. </p>
<p><u>TPP  approach</u><br>  
TPP based on scientific study and <strong>TPPP approach</strong>, carries out the systematic study and understands the product, process, customer requirements, and technology and work difficult level. </p>

<p>The potential analysis of exiting manpower is also done and competence level is determined. TPP has developed unique method of doing the study through tools and technique. <strong>Z concept and DDAMAIR</strong> approach is used for the same. </p>
<p>We carry out the study of Organization – Functions – Process- People. Starting from Business Plan to work plan study, 9 specialized documents are used and data collected and analyzed. We study all 14 functions , 200 processes and sub processes and all manpower to carry out the study .</p>

<p>The present and future business requirement as well as man, machine, method and material – all 4 M study is done. We understands first the “As Is” position and then “to be”  position is mapped. In depth understanding is carried out. </p>

<p>5 level discussion and interaction takes place at different level and we determined the manpower required. We cover up all levels and understand the manpower requirement. Presentation is made at different levels. We also support in implementation of the project report.</p>
<p><strong>Clients benefits </strong><br>
We have been able to save number of manpower. In our one of the latest study we have been able to save more than 100 manpower. Had this study not been conducted then organization could have added the manpower and overall productivity and performance would have been down.
</p>
