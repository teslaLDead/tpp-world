    <div id="e" class="tab-pane fade">
  <h3 class="secand-sec-bluebg"><strong>Demanding Performance - Front Line</strong></h3>
<p><strong>Challenges</strong><br> Developing the performance parameters for front line team members (Associate level ) has always been the challenging one. The client have has finding it difficult the decide the group level appraisal system. At the end of the year a format is sent to the HoD/ Section Head and asked to give the rating of all front line team members. Difficult to justify the rating as no parameters to measure. More perception based rather than objectivity. Level of dissatisfaction among employees.</p>

<p><strong>TPP approach </strong><br>
TPP has introduced research based performance management system for the front line team members. This is based on credit system. All together 9 areas based on PQCDSEM and discipline and participation have been identified and total 21 areas have been selected.  The performance parameters and targets and discussed with the person concerned and agreed.</p> 

<p>There is credit and debit point. In the beginning of the appraisal period 100 points are credited and there is provision of debit and bonus points. In case there  is non-conformity / deviation then there is pro rata basis deduction of points in decimal and at the same time for giving bonus for good points. overall 25 points bonus given to employees for the good work. One can get points upto 125 with additional bonus points.</p>

<p>The transparency in target setting and employees knows which work would add bonus and which non conformity would get the point deducted , this has given confidence to employees and there is increase in productivity and performance. overall per person the productivity has gone up.</p>  

<p><strong>Clients Benefits </strong><br>
There is trust , respect and reliability. Overall productivity and performance has increased. There is openness and transparency. The wastage has been reduced and productivity has increased. For last 2 years there have been consistent result and also implemented in other group companies.</p>

