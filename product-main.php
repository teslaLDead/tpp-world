      <h3 class="secand-sec-bluebg"><strong>Developing competency - Middle Level </strong><span class="pull-right"><a href="pdf-folder/Developing competence-Middle Level.pdf"><img src="images/pdf-icon-2.jpg" alt="" data-toggle="tooltip" title="Download pdf"/></a></span> </h3>
    <p>  Developing competencies in the form of leadership has been key achievement of TPP. In last one decade number of leadership projects have been completed. </p>

<p><strong>Challenges </strong><br>
Leadership development at top and middle level is the challenging one. To deliver the productivity and performance and achieve the desired business result, it is very much required that the business leaders as well as the functional leaders play an important role. There has always been performance gap, which has been addressed by the leadership development project. Leaders have been developed across the level, be it front level or middle or top level. Through TPP initiatives, number of success stories have been developed.</p>

<p><strong>TPP Approach </strong><br>
TPP has developed research based leadership development approach which is based on TPPP and MSK operating model. We focus both on hard and soft elements. Based on potential analysis of each team members the intervention and feedback is decided. There are 3 classroom modules – Win-Win, Manthan and Mala which are conducted in a gap of 60 days from each module supported by in-depth analysis and report. There has been in-depth understanding of all particiaptns during the programme and after the programme and based on initiative undertaken and initiative shown, the development plan has been prepared by TPP team. There are 10 points development plan , which is monitored and analyzed. The activity and analysis is designed and developed by TPP. Complete adult learning processes have been followed. </p>

<p>The multi-dimensional analysis is done based on involvement during the programme, activity being carried out , home work done , behavior /improvement done at the work place, all are considered. There is always scope for each and every individual to discuss and agree for the same. </p>

<p><strong>Clients’ benefits</strong><br>
Breakthrough result have been achieved. there has been increase in overall productivity and performance. Accountable team developed which have been able to deliver the desired result. The team has also been able to develop others in their work areas and carried out the improvement.</p> 

