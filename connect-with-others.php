<p>3Cs always support in success. Before we communicate with others , we need to connect with others. Know the person name , his background and interest . greet him / her first before starting the verbal communication.  We have to connect first and when there is willingness from the person or the person is ready then startVerbal communicating. All communication must end with commitment – what next – what is going to be done after this . what we require to do the next and when we would be back. There is need to follow up. </p>
</div>


<div id="b">
<h3 class="secand-sec-bluebg heading-sec-2">Assertive communication skills</span></h3>
<p>The communication with others to be assertive. We need not be aggressive or passive in our communication. Our tone, body language , gesture and distance between the 2 or more should be equal. There is no need to become aggressive in our approach . develop the habit of good listening and understanding . understand first then speak. The listening is an art and it has to be practiced. Becoming aggressive time and again becomes barrier in communication and it also affects our relations with others. </p>
</div>

