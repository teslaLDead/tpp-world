<p><strong>Challenges</strong></p> 

<p>A decade long intervention at various plants and locations for middle level resulted in overall enhancing people productivity and performance. Being  an old organization since 1968, there have been number of challenges in employees participation and involvement.</p>

<p>TPP intervention has been in different forms starting from employee involvement to leadership development for the middle level. </p>

