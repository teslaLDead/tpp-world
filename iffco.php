<p><strong>TPP intervention and Approach</strong></p> 

<p>TPP intervention has been in different form. The latest one for front line and middle level leaders development has been very successful one.  In one of the plant selected 40 front line and middle level managers for leadership development. </p>

<p>6 months development programme designed and developed with the support of corporate HR, plant HR and technical team. The intervention focused on 3Cs, 3Is and 3Ls. 10 traits were identified – connect, communicate, commitment, involve , improve, impact, lead, lead and learnt and setting example for others. 3 modules based on theme implemented in a period of 6 months and closely monitored for the development. Based on potential analysis approach , the performance and progress were monitored after each module. Participants, respective HoDs, subordinate and internal customers were contacted and their feedback and observations were recorded.</p> 

<p>During the intervention number of gaps identified and addressed.</p> 

<p>There has been close monitoring and feedback through one on one session as well as feedback during the presentation given by the respective participants. Development plan monitored and report shared at different levels.
</p>
