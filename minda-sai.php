<p>   Delivering Performance </p>

<p>An auto component manufacture supplying wiring harnesses to 2 and 4 wheelers.</p> 

<p><strong>Challenges</strong></p> 

<p>Getting performances from front line team members. There has been number of challenges at the shop floor. Overall operational efficiency to be improved. There has been challenges related with productivity and quality. How to improve the overall cell efficiency has been the key focused areas. The front line team leaders have to play an important role in getting the performance at the shop floor. Team members commitment and involvement in meeting the customer demand.</p> 

<p>Team leaders to motivate and guide the front line team members to deliver the result. Focus on product quality.</p>  

<p><strong>TPP intervention and Approach </strong></p>

<p>Based on briefing received from the plant head, operation head and HR head, the intervention was designed and developed. Shop floor visit was made and processes and sub processes were understood. The performance data was also analyzed and gaps were identified.</p> 

<p>The intervention was based on 3 Cs and 3 Is , how to connect the team leaders and team members in achieving the target. Making the team leaders accountable for getting the required performance from the front line team members.</p> 

<p>3 Cs focused on connect- communicate- commitment – how to connect with each other and understand others needs. To develop the effective interpersonal relations and team work. Motivating the front line team members to own the processes and sub processes and produce the defect free product. </p>

<p>3Is- involve- improve – impact .the idea was to get 200 percent involvement of each and every team member. Getting to the root of the reason and understanding the same. Focus on creativity and innovation. Bringing best ideas for improvement. </p>

<p>Based on potential analysis and post training home work , the development plan for each team leaders was worked out and one on one mentoring and coaching was done. There was also follow up and presentation given by the team leaders. </p>


<p><strong>Impact</strong></p> 

<p>Overall improvement in operational efficiency was observed at the shop floor. There was improvement in productivity and performance at the shop floor. Team leader owned the responsibility to bring changes at the shop floor. Along with productivity, quality, cost, delivery, safety and 5 S was also focused. Team work and leadership quality improved. </p>

