<p>Demanding Performance </p>

<p>The manufacturer of wide range Grinders in CNC, PLC and Hydraulic versions. Leader in their own field.</p> 

<p>TPP association with the company has been in 2 forms – one for the shop floor efficiency improvement and second one on demanding performances from the front line team members being an engineering company, most of the front line team members have been highly skilled an experienced one.</p> 

<p><strong>Challenges</strong></p> 

<p>Getting performance and monitoring the performance has always been challenging one. When the front line team members are highly skilled and experienced one, the onus of getting the performance and desired result is very much with the front line team leaders and functional heads. Managing the smart team members have always been challenging for the team leaders. How to improve the accountability at each and every level was the key challenge. </p>

<p>Even in the performance management, demanding performance and getting the performance requires a strategic approach. How to demand the performance based on set targets always challenging. Organization given rewards and recognition to all front line team members , still the level of satisfaction was not there. </p>

<p><strong>TPP intervention and Approach</strong></p> 

<p>Based on management input and expectation, the intervention was designed and developed. The 1st intervention was in the form of getting the performance at the shop floor. Through SMART People, Good work concept , number of workshops were conducted and front line team members were guided and supported in improving the performance. The potential analysis of each team members was done and guidance and support were provided. </p>

<p>TPP unique intervention had been in the form of designing and developing the performance management system for the front line team members. An unique system was developed and implemented which was based on credit and debit system. 9 areas of operational and personal excellence were selected with 21 parameters. In the beginning of the appraisal period the given targets were agreed between the team members and functional leaders. For every good work bonus points were added and for any losses or low performance, the points were debited. An excellent system which is very effective and able to meet the business demand.</p> 

<p><strong>Impact</strong></p> 

<p>There has been high  level satisfaction in the front level team members’ performance management in terms of target setting , review and appraisal. The unique method develop a level of trust and fair understanding. The accountability and performance level also improved. The new system being practiced for last 2 years and also implemented in other plants. </p>

